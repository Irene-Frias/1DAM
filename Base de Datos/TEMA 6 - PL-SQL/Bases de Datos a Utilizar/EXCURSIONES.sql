
CREATE TABLE RECURSO (
IDRecurso VARCHAR2(3) PRIMARY KEY,
Nombre VARCHAR2(50) UNIQUE,
Stock number(3)
);

CREATE TABLE RUTA (
IDRuta VARCHAR2(3) PRIMARY KEY,
Nombre VARCHAR2(50) UNIQUE,
Descripcion VARCHAR2(50),
Precio DECIMAL(6,2),
Tipo varchar2(10) check (Tipo in ('Fácil', 'Media', 'Difícil', 'Extrema'))
);

CREATE TABLE USA (
IDRuta VARCHAR2(3),
IDRecurso VARCHAR2(3),
Cantidad number(3),
CONSTRAINT USA_PK PRIMARY KEY(IDRuta, IDRecurso),
CONSTRAINT USA_RE_FK FOREIGN KEY (IDRecurso) REFERENCES  RECURSO (IDRecurso) ON DELETE CASCADE,
CONSTRAINT USA_RU_FK FOREIGN KEY (IDRuta) REFERENCES  RUTA (IDRuta) ON DELETE CASCADE
);

CREATE TABLE EMPLEADO (
DNI VARCHAR2(9) PRIMARY KEY,
Nombre VARCHAR2(50),
Apellidos VARCHAR2(50),
FNac date,
Salario DECIMAL(6,2)
);

CREATE TABLE USUARIO (
IDUsuario VARCHAR2(5) PRIMARY KEY,
DNI VARCHAR2(9),
Nombre VARCHAR2(50),
Apellidos VARCHAR2(50),
Telefono VARCHAR2(9),
Edad number(3)
);

CREATE TABLE EXCURSION(
IDExcursion VARCHAR2(5) PRIMARY KEY,
Nombre VARCHAR2(50),
HoraInicio timestamp,
HoraFin timestamp,
NumPlazas number(3),
IDRuta VARCHAR2(3),
IDEmpleado VARCHAR2(9),

CONSTRAINT EX_RU_FK FOREIGN KEY (IDRuta) REFERENCES  RUTA (IDRuta) ON DELETE CASCADE,
CONSTRAINT EX_EM_FK FOREIGN KEY (IDEmpleado) REFERENCES EMPLEADO (DNI) ON DELETE CASCADE
);

CREATE TABLE RESERVA(
IDExcursion VARCHAR2(5),
IDUsuario VARCHAR2(5),
Importe DECIMAL(6,2),
FormaPago varchar2(10) check (FormaPago in ('Tarjeta', 'Contado', 'Bizum')),

CONSTRAINT RE_PK PRIMARY KEY(IDExcursion, IDUsuario),
CONSTRAINT RE_EX_FK FOREIGN KEY (IDExcursion) REFERENCES  EXCURSION (IDExcursion) ON DELETE CASCADE,
CONSTRAINT RE_US_FK FOREIGN KEY (IDUsuario) REFERENCES USUARIO (IDUsuario) ON DELETE CASCADE
);

CREATE TABLE USUARIOS_BORRADOS (
DNI VARCHAR2(9),
Nombre VARCHAR2(50),
Telefono VARCHAR2(9),
FechaBorrado date
);

